package com.faceproject.beans;

public class EmployeeInfo {

	private Integer eiId;
	private String eiName;
	private String eiFaceInfo;
	private Integer eiJob;
	private String ejProfession;
	
	public Integer getEiId() {
		return eiId;
	}
	public void setEiId(Integer eiId) {
		this.eiId = eiId;
	}
	public String getEiName() {
		return eiName;
	}
	public void setEiName(String eiName) {
		this.eiName = eiName;
	}
	public String getEiFaceInfo() {
		return eiFaceInfo;
	}
	public void setEiFaceInfo(String eiFaceInfo) {
		this.eiFaceInfo = eiFaceInfo;
	}
	public Integer getEiJob() {
		return eiJob;
	}
	public void setEiJob(Integer eiJob) {
		this.eiJob = eiJob;
	}
	public String getEjProfession() {
		return ejProfession;
	}
	public void setEjProfession(String ejProfession) {
		this.ejProfession = ejProfession;
	}
}
